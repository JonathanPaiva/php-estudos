$idade = 28;

//Concatenação em strings, uso de aspas simples e duplas

//uso de aspas duplas permite o uso das variáveis diretamente no texto escrito, sem precisar caracteres de concatenação.
echo "Olá Mundo!, minha idade é: $idade Anos.";

//uso de aspas simples necessário usar caracteres 'ponto ( . )' para concatenar variáveis ao texto indicado.
echo 'Olá Mundo!, minha idade é: ' . $idade . ' Anos.';

